int add(int a, int b){
    return a+b;
}

int soust(int a, int b){
    return a-b;
}

int mult(int a, int b){
    return a*b;
}

int div(int a, int b){
    return a/b;
}

int mod(int a, int b){
    return a%b;
}

int et(int a, int b){
    return a&&b;
}

int ou(int a, int b){
    return a||b;
}

int neg(int a){
    return ~a;
}